
 weather = "rainy"

 case weather 
	 when 'rainy'
	 	puts 'You better wear boots!'
	 when 'sunny'
	 	puts "Lather up some sunscreen and go to the pool!"
	 when 'cloudy'
	 	puts "Take an umbrella, just in case..and wear a coat!"
	 when 'HOT'
	 	puts 'Stay inside and crank the AC! Or, go jump in a lake!'
	 else
	 	puts 'Well, shoot, what kind of day is it?'
 end
